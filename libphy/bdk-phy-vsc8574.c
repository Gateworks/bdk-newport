/**
 * All SMI/MDIO accesses in this driver use this wrapper
 *
 * @param device
 * @param location
 *
 * @return
 */
static int mdio_read(bdk_device_t *device, int location)
{
    bdk_node_t node = device->node;
    int mdio_bus = device->priv.phy.smi;
    int phy_addr = device->priv.phy.address;
    return bdk_mdio_read(node, mdio_bus, phy_addr, location);
}

/**
 * All SMI/MDIO accesses in this driver use this wrapper
 *
 * @param device
 * @param location
 *
 * @return
 */
static int mdio_write(bdk_device_t *device, int location, int val)
{
    bdk_node_t node = device->node;
    int mdio_bus = device->priv.phy.smi;
    int phy_addr = device->priv.phy.address;
    return bdk_mdio_write(node, mdio_bus, phy_addr, location, val);
}

/**
 * Setup VSC8574 PHYs for either SGMII or QSGMII
 *
 * @param device
 * @param qsgmii
 *
 * @return
 */
static int setup_vsc8574_phy(bdk_device_t *device, bool qsgmii)
{
    int mdio_addr = device->priv.phy.address;

    for (int port = 0; port < 4; port++)
    {
        /* Temporarily change the PHY address so we can loop through the 4 ports */
        device->priv.phy.address = mdio_addr + port;

        // Select "G" registers
        mdio_write(device, 31, 0x10);
        // Reg 19G, bit 15:14
        // 0 = SGMII
        // 1 = QSGMII
        int reg19 = mdio_read(device, 19);
        int reg18;
        if (qsgmii)
        {
            // QSGMII
            reg19 = bdk_insert(reg19, 1, 14, 2);
            reg18 = 0x80e0;
        }
        else
        {
            // SGMII
            reg19 = bdk_insert(reg19, 0, 14, 2);
            reg18 = 0x80f0;
        }
        mdio_write(device, 19, reg19);
        // Write 18G, change all 4 ports
        mdio_write(device, 18, reg18);
        // Select main registers
        mdio_write(device, 31, 0);
        // Reg23, 10:8 Select copper
        int reg23 = mdio_read(device, 23);
        reg23 = bdk_insert(reg23, 0, 8, 3);
        mdio_write(device, 23, reg23);
        // Reg0, Reset
        int reg0 = mdio_read(device, 0);
        reg0 = bdk_insert(reg0, 1, 15, 1);
        mdio_write(device, 0, reg0);
        // Reg 16E3, bit 7 auto negotiation
        mdio_write(device, 31, 3);
        mdio_write(device, 16, 0x80);
        // Select main registers
        mdio_write(device, 31, 0);
        // Change the LED setup
        mdio_write(device, 29, 0x1321);

        // Dump registers
        if (false)
        {
            printf("%s: Register dump, mode %s\n",
                   device->name, (qsgmii) ? "QSGMII" : "SGMII");
            device->priv.phy.address = 4;
            for (int reg_set = 0; reg_set <= 0x10; reg_set += 0x10)
            {
                printf("  Dump registers with reg[31]=0x%x\n", reg_set);
                mdio_write(device, 31, reg_set);
                for (int reg=0; reg < 32; reg++)
                    printf("    reg[%02d]=0x%x\n", reg, mdio_read(device, reg));
            }
        }

    }
    /* Restore the PHY address */
    device->priv.phy.address = mdio_addr;
    return 0;
}

static void wr_masked(bdk_device_t *device, int reg, int value, int mask)
{
    int nmask = ~mask;
    int old = mdio_read(device, reg);
    int vmask = value & mask;
    int newv = old & nmask;
    newv = newv | vmask;
    mdio_write(device, reg, newv);
}

static void vsc8574_program(bdk_device_t *device)
{
    mdio_write(device, 31, 0x0010);
    mdio_write(device, 18, 0x800f);
    mdio_write(device, 31, 0x0010);

    int reg18g = mdio_read(device, 18);
    int timeout = 10;
    while ((reg18g & (1<<15)) && (timeout > 0))
    {
        bdk_wait_usec(100000);
        reg18g = mdio_read(device, 18);
        timeout = timeout - 1;
    }
    if (timeout == 0)
        bdk_error("%s: Timeout waiting for complete\n", device->name);

    mdio_write(device, 31, 0x0000);
    mdio_write(device, 31, 0x0010);
    wr_masked(device, 12, 0x0000, 0x0800);
    mdio_write(device,  9, 0x005b);
    mdio_write(device, 10, 0x005b);
    wr_masked(device, 12, 0x0800, 0x0800);
    mdio_write(device, 18, 0x800f);
    wr_masked(device, 0, 0x0000, 0x8000);
    mdio_write(device, 18, 0x0000);
    wr_masked(device, 12, 0x0000, 0x0800);
    mdio_write(device, 31, 0x10);
    mdio_write(device, 0, 0x7009);
    mdio_write(device, 12, 0x5002);
    mdio_write(device, 11, 0x0000);

    for (unsigned int i=0; i<sizeof(patch_arr); i++)
    {
        int d = 0x5000 | patch_arr[i];
        mdio_write(device, 12, d);
    }
    mdio_write(device, 12, 0x0000);

    mdio_write(device,  3, 0x3eb7);
    mdio_write(device,  4, 0x4012);
    mdio_write(device, 12, 0x0100);
    mdio_write(device,  0, 0x4018);
    mdio_write(device,  0, 0xc018);

    // below verifies CRC is correct in 8051 RAM.  CRC is 16-bit.
    mdio_write(device, 31, 0x0001);
    mdio_write(device, 25, 0x4000);
    mdio_write(device, 26, sizeof(patch_arr) + 1);
    mdio_write(device, 31, 0x0010);
    mdio_write(device, 18, 0x8008);

    reg18g = mdio_read(device, 18);
    timeout = 10;
    while ((reg18g & (1<<15)) && (timeout > 0))
    {
        bdk_wait_usec(100000);
        reg18g = mdio_read(device, 18);
        timeout = timeout - 1;
    }
    if (timeout == 0)
        bdk_error("%s: Timeout waiting for complete\n", device->name);

    mdio_write(device, 31, 0x0001);

    int crc_calculated = mdio_read(device, 25);
    if (crc_calculated != 0xB7C2)
        printf("8051 crc_calculated = 0x%x, expected_crc = 0x%x\n", crc_calculated, 0xB7C2);

    mdio_write(device, 31, 0x0000);
}

static void add(bdk_device_t *device, const char *config_str)
{
    device->priv.phy.smi = bdk_driver_extract_int(config_str, "smi", 0, 31);
    device->priv.phy.address = bdk_driver_extract_int(config_str, "addr", 0, 31);
    device->priv.phy.priv = NULL;
    int qlm = bdk_driver_extract_int(config_str, "qlm", 0, bdk_qlm_get_num(device->node) - 1);
    int qlm_lane = bdk_driver_extract_int(config_str, "qlm_lane", 0, 3);
    BDK_TRACE(DEVICE, "%s: VSC8574 PHY on SMI=%d, ADDR=%d, QLM=%d, QLM_LANE=%d\n",
        device->name, device->priv.phy.smi, device->priv.phy.address, qlm, qlm_lane);

    int num_lanes = bdk_qlm_get_lanes(device->node, qlm);
    if (qlm_lane + 1 >= num_lanes)
    {
        /* Lanes are swizzled */
        for (int slice = 0; slice < 4; slice++)
        {
            if (qlm_lane < 0)
            {
                qlm--;
                qlm_lane = num_lanes - 1;
            }
            bdk_netphy_register(device, slice, device->node, qlm, qlm_lane);
            qlm_lane--;
        }
    }
    else
    {
        /* Lanes follow normal ordering */
        for (int slice = 0; slice < 4; slice++)
        {
            if (qlm_lane > num_lanes)
            {
                qlm++;
                qlm_lane = 0;
            }
            bdk_netphy_register(device, slice, device->node, qlm, qlm_lane);
            qlm_lane++;
        }
    }
}

static bdk_if_link_t phy_get_speed(bdk_device_t *device, bdk_netphy_t phy)
{
    bdk_if_link_t result;
    result.u64 = 0;

    int mdio_addr = device->priv.phy.address;
    device->priv.phy.address += phy.s.slice;

    /* Auxiliary Control and Status, Address 28 (0x1C) */
    int phy_status = mdio_read(device, 0x1c);
    int phy_status2 = mdio_read(device, 0x01);

    device->priv.phy.address = mdio_addr;

    result.s.full_duplex = (phy_status>>5)&1;
    switch ((phy_status>>3) & 3)
    {
        case 0:
            result.s.speed = 10;
            result.s.up = 1;
            break;
        case 1:
            result.s.speed = 100;
            result.s.up = 1;
            break;
        default:
            result.s.speed = 1000;
            break;
    }
    result.s.up = (phy_status2>>2)&1;

    return result;
}

static int phy_init(bdk_device_t *device, bdk_netphy_t phy)
{
    /* Check if the PHY is VSC8574 PHY we expect */
    int phy_status = mdio_read(device, BDK_MDIO_PHY_REG_ID1);
    if (phy_status != 0x0007)
    {
        bdk_error("%s: Not a VSC8574 PHY\n", device->name);
        return -1;
    }
    phy_status = mdio_read(device, BDK_MDIO_PHY_REG_ID2);
    if ((phy_status & 0xfff0) != 0x04a0)
    {
        bdk_error("%s: Not a VSC8574 PHY\n", device->name);
        return -1;
    }

    /* Only program once */
    if (device->priv.phy.priv)
        return 0;
    device->priv.phy.priv = device;

    printf("%s: Programming VSC8574 PHY\n", device->name);

    /* Program the VSC8574 PHY */
    vsc8574_program(device);

    /* Switch the VSC8574 PHY to the correct mode */
    bdk_qlm_modes_t qlm_mode = bdk_qlm_get_mode(device->node, phy.s.qlm, phy.s.qlm_lane);
    bool is_qsgmii = (qlm_mode == BDK_QLM_MODE_QSGMII);
    printf("%s: Setting VSC8574 PHY to %s\n", device->name, is_qsgmii ? "QSGMII" : "SGMII");
    return setup_vsc8574_phy(device, is_qsgmii);
}

static int phy_loopback(bdk_device_t *device, bdk_netphy_t phy, bool host_side, bool line_side)
{
    int mdio_addr = device->priv.phy.address;
    device->priv.phy.address += phy.s.slice;

    /* Select main registers */
    mdio_write(device, 31, 0);

    /* Near end loopback, host side */
    if (host_side)
    {
        int reg = mdio_read(device, 0);
        reg = bdk_insert(reg, 1, 14, 1);
        mdio_write(device, 0, reg);
    }

    /* Far end loopback, line side */
    if (line_side)
    {
        int reg = mdio_read(device, 23);
        reg = bdk_insert(reg, 1, 3, 1);
        mdio_write(device, 23, reg);
    }

    device->priv.phy.address = mdio_addr;
    return 0;
}

